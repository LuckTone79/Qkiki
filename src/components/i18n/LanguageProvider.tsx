export type AppLanguage = "en" | "ko" | "ja" | "es";

export const SUPPORTED_LANGUAGES: readonly AppLanguage[] = [
  "en",
  "ko",
  "ja",
  "es",
] as const;

export function isAppLanguage(value: unknown): value is AppLanguage {
  return (
    value === "en" || value === "ko" || value === "ja" || value === "es"
  );
}

/**
 * Picks the string for the active language from a set of per-language
 * variants. Used for inline copy that lives outside the shared dictionary.
 */
export function localize(
  language: AppLanguage,
  variants: Record<AppLanguage, string>,
): string {
  return variants[language] ?? variants.en;
}

/** BCP-47 locales for Intl APIs (date/number formatting). */
export const INTL_LOCALES: Record<AppLanguage, string> = {
  en: "en-US",
  ko: "ko-KR",
  ja: "ja-JP",
  es: "es-ES",
};

export function intlLocale(language: AppLanguage): string {
  return INTL_LOCALES[language] ?? INTL_LOCALES.en;
}

/**
 * Narrows the active language to the keys an en/ko-only text table actually
 * provides. The staff-only admin panel is translated for en/ko and falls back
 * to English for other languages.
 */
export function adminTextKey(language: AppLanguage): "en" | "ko" {
  return language === "ko" ? "ko" : "en";
}

